import math

# round to biggest integre
print("math.ceil(1.4) is: ", math.ceil(1.4)) # 2
print("math.ceil(5.3) is: ", math.ceil(5.3)) # 6
print("math.ceil(-5.3) is: ", math.ceil(-5.3)) # -5
print(math.ceil(22.6)) # 23
print(math.ceil(10.0)) # 10


# round to smaller integer
print("math.floor(1.4) is: ", math.floor(1.4)) # 1
print("math.floor(5.3) is: ", math.floor(5.3)) # 5
print("math.floor(-5.3) is: ", math.floor(-5.3)) # -5
print(math.floor(22.6)) # 23
print(math.floor(10.0)) # 10
